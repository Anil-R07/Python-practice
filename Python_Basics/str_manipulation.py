string = "Welcome All to This Page "
# Upper case 
print(string.upper())

# Lower case 
print(string.lower())

# strip - Remove spaces at last
print(string.strip())

# replace 
name = "John" 
print(name.replace("John", "Joseph"))

# Accessing string characters 
print(string[5])
print(string[-5])
print(string[5:16]) # slicing 
print(string[:8]) # from start to 8th index
print(string[3:]) # from 3rd index to end 

# Escape sequesnce 
